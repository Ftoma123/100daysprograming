# Create a Tuple
tuple1 = ('A','B','C')
print(tuple1)

# empty tuple and tuble with one value
tuple2 = ()
tuple3 = ('A',)
print(tuple2)
print(tuple3)

# Access tuple item
tuple4 = ('Apple','Banana','Cat')
print(tuple4[1])

# loop in tuple
tuple5 = ('Apple','Banana','Cat')
for x in tuple5:
    print(x)


