# Printing List []
list = [10, 20, 30]
print(list)

# Access Items 
list1 = ["apple", "banana", 'Grape']
print(list1[2])

# Loop Through a List 
list2 = [1, 2, 3, 4, 10, 20, 30]
for x in list2:
    print(x)

# Change Item Value
list3 = ["apple", "banana", 'Grape']
list3[1] = "Strawberry"
print(list3)

# Remove Item Value
list4 = ["apple", "banana", 'Grape']
del list4[1]
print(list4)

#Remove whole list
list5 = ["apple", "banana", 'Grape']
del list5
print(list5)